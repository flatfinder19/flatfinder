m = 91
stateCities = []
def split(names):
  
  l = len(list(names))

  for i in range(l):
    stateCities.append((names[str(i)].replace("\\", "").replace("/","")))

  for j in range(l):
    sub = stateCities[j].split(",")
    if(len(sub) == 2):
      stateCities[j] = {"name": sub[0], "state": sub[1]}
    else:
      stateCities[j] = {"name": sub[0], "state": ''}


def putSite(sites):
  for i in range(m):
    n = {
        "name": stateCities[i]["name"],
        "state": stateCities[i]["state"],
        "site": sites[str(i)]
    }
    stateCities[i] = n

